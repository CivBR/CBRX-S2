FROM Civilizations a, Units b WHERE a.Type != 'CIVILIZATION_MC_JERUSALEM' 
AND b.Type IN ('UNIT_MC_JERUSALEM_CRUSADER_TEMPLARS', 'UNIT_MC_JERUSALEM_CRUSADER_TEUTONIC', 'UNIT_MC_JERUSALEM_CRUSADER_ST_JOHN', 'UNIT_MC_JERUSALEM_CRUSADER_SEPULCHRE', 'UNIT_MC_JERUSALEM_CRUSADER_LAZARUS', 'UNIT_MC_JERUSALEM_CRUSADER_CRUCIFORM');

