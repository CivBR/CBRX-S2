AFTER INSERT ON Civilizations
WHEN NEW.Type != 'CIVILIZATION_MC_JERUSALEM'
BEGIN
	INSERT INTO Civilization_UnitClassOverrides  
			(CivilizationType, 	UnitClassType, 	UnitType) 
