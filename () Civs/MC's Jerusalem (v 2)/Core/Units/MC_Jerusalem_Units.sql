	FROM Units WHERE Type IN ('UNIT_MC_JERUSALEM_CRUSADER_TEMPLARS', 'UNIT_MC_JERUSALEM_CRUSADER_TEUTONIC', 'UNIT_MC_JERUSALEM_CRUSADER_ST_JOHN', 'UNIT_MC_JERUSALEM_CRUSADER_SEPULCHRE', 'UNIT_MC_JERUSALEM_CRUSADER_LAZARUS', 'UNIT_MC_JERUSALEM_CRUSADER_CRUCIFORM');
