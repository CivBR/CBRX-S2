    WHERE Type IN ('TECH_ENVIRONMENTALISM', 'TECH_CLONING', 'TECH_GEN_MANIP', 'TECH_MEMETICS', 'TECH_GRID_COMPUTING', 'TECH_TRANSGENICS', 'TECH_AUTONOMOUS_SYSTEMS',
