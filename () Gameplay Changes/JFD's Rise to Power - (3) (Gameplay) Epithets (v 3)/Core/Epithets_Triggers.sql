CREATE TRIGGER RTP_Eras
AFTER INSERT ON Eras 
WHEN NEW.Type = 'ERA_ENLIGHTENMENT'
BEGIN
	UPDATE Eras
	SET EpithetProgressThreshold = 100
	WHERE Type = 'ERA_ENLIGHTENMENT';
END;
