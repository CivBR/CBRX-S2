--Player:CanAdoptNeutralityIdeology
local eraModernID = GameInfoTypes["ERA_MODERN"]
function Player.CanAdoptNeutralityIdeology(player)
	if player:GetCurrentEra() >= eraModernID and player:GetLateGamePolicyTree() == -1 then
		return true
	end
	return false
end	
